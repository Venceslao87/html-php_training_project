<?php

// Знаходження частки двох чисел, та остачі, якщо вона є в явності

$a=19;
$b=2;
$result=$a/$b;
$modulo=$a%$b;

if ($modulo==0) {
    echo "Числа " ."$a". " та " ."$b". " діляться націло"."<br>";
    echo 'Частка чисел ' ."$a". ' та ' ."$b". ' дорівнює ' ."$result";
} else {
    echo "Числа " ."$a". " та " ."$b". " діляться з остачею"."<br>";
    echo 'Остача чисел ' ."$a". ' та ' ."$b". ' дорівнює ' ."$modulo";
}