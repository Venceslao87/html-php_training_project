<?php

// З двох чисел, парного чи непарного вивести парне число.

$a=7;
$b=7;

$mod1=$a%2;
$mod2=$b%2;
if ( $mod1==0 and $mod2==0) {
    echo 'Обидва числа парні';
} elseif ($mod1!=0 and $mod2==0) {
    echo 'Число '."$a". ' — непарне, а число '."$b". ' - парне';
} elseif ($mod1==0 and $mod2!=0) {
    echo 'Число '."$a". ' — парне, а число '."$b". ' - непарне';
} else {
    echo 'Обидва числа непарні';
}
